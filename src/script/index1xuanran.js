! function($) {
    function scroll() {
        let top = $(window).scrollTop();
        top > 500 ? $('.louti').show() : $('.louti').hide();
        $('.louceng').each(function(index, element) {
            let loucengtop = $(this).offset().top;
            if (loucengtop >= top) {
                // $('.louti .lounav').removeClass('active');
                $('.louti .lounav').eq($(this).index()).addClass('active');
                return false;
            }
        });
    }
    scroll();
    $(window).on('scroll', function() {
        scroll();
    });
    $('.louti .lounav').on('click', function() {
        $(this).addClass('active').siblings().removeClass('active');
        let loucengtop = $('.louceng').eq($(this).index()).offset().top;
        $('html').stop(true).animate({
            scrollTop: loucengtop
        })
    });
    $('.to-top').on('click', function() {
        $('html').stop(true).animate({
            scrollTop: 0
        })
    });
}(jQuery);

