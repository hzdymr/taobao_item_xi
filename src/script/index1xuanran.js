function getStyle(obj, attr) { //obj:运动的物体   attr:运动物体的属性
    if (window.getComputedStyle) { //标准
        return window.getComputedStyle(obj)[attr];
    } else {
        return obj.currentStyle[attr];
    }
}

function bufferMove(obj, json, fn) { //obj:运动的物体   json：属性对象   fn:函数(函数做参数，回调函数)
    let speed = 0;
    clearInterval(obj.timer);
    obj.timer = setInterval(() => {
        let flag = true;
        //遍历属性attr，属性值
        for (let attr in json) { //attr:属性名   json[attr]:目标值
            //求当前值(透明度有问题)
            let currentvalue = null;
            if (attr === 'opacity') { //透明度
                currentvalue = getStyle(obj, attr) * 100; //将透明度的值扩大100倍。
            } else {
                currentvalue = parseInt(getStyle(obj, attr));
            }
            //求缓冲的速度
            speed = (json[attr] - currentvalue) / 5;
            speed = speed > 0 ? Math.ceil(speed) : Math.floor(speed);

            //物体的运动和停止
            if (currentvalue !== json[attr]) { //当前的某个属性没有到目标点，继续运动
                //透明度有问题,透明度没有单位
                if (attr === 'opacity') {
                    obj.style.opacity = (currentvalue + speed) / 100; //缩小100倍，0-1之间的值进行赋值
                } else {
                    obj.style[attr] = currentvalue + speed + 'px';
                }
                //只有物体还在运动
                flag = false;
            }
        }

        if (flag) { //flag ===true for没有改变flag的值,运动的结束了。
            clearInterval(obj.timer);
            fn && typeof fn === 'function' && fn(); //上一次运动停止了 如果fn存在，fn是一个函数，执行fn，否则不执行。
        }


    }, 10)
}

function carousel() {
    //获取元素
    this.carousel = document.querySelector('.carousel');
    this.list = document.querySelector('.carousel ul');
    this.picli = document.querySelectorAll('.carousel ul li'); //6张图
    this.picbtn = document.querySelectorAll('.carousel div i'); //5个圆圈
    this.leftarrow = document.querySelector('#arrowleft');
    this.rightarrow = document.querySelector('#arrowright');
    this.index = 0; //存储索引的变量。
    this.timer = null;

}

carousel.prototype.init = function() {
    //1.改变了布局，重新设置ul的width
    this.list.style.width = this.picli.length * this.picli[0].offsetWidth + 'px';
    this.liwidth = this.picli[0].offsetWidth; //一个li的宽度。
    //2.给picbtn添加点击事件。
    for (let i = 0; i < this.picbtn.length; i++) {
        this.picbtn[i].onclick = () => {
            this.index = i - 1;
            //this.tabswitch有一个this.index++;
            this.tabswitch()
        }
    }
    //3.左右箭头事件。
    this.rightarrow.onclick = () => {
        this.tabswitch();
    }

    this.leftarrow.onclick = () => {
            //本身减1  tabswitch有加1
            this.index -= 2;
            this.tabswitch();
        }
        //4.自动播放
    this.timer = setInterval(() => {
        this.rightarrow.onclick();
    }, 3000);

    this.carousel.onmouseover = () => {
        clearInterval(this.timer);
    }
    this.carousel.onmouseout = () => {
        this.timer = setInterval(() => {
            this.rightarrow.onclick();
        }, 3000);
    }
};

//封装一个方法将切换的过程单独封装。
carousel.prototype.tabswitch = function() {
    this.index++;
    if (this.index === this.picbtn.length + 1) { //右箭头
        this.list.style.left = 0 + 'px'; //从0开始运动完一张。
        this.index = 1; //第二张图片进来
    }
    if (this.index === -1) { //左键头
        this.list.style.left = -this.liwidth * this.picbtn.length - 1 + 'px';
        this.index = this.picbtn.length - 1; //索引4
    }
    for (let i = 0; i < this.picbtn.length; i++) {
        this.picbtn[i].className = ''
    }
    if (this.index === this.picbtn.length) { //this.picbtn.length:5
        this.picbtn[0].className = 'active';
    } else {
        this.picbtn[this.index].className = 'active';
    }
    bufferMove(this.list, {
        left: -this.index * this.liwidth
    });
}
new carousel().init(); //实例化+调用初始化方法。

